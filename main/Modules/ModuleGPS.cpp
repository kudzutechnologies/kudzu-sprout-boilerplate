void ubx_checksum_update(uint8_t * csum, const uint8_t * data, const size_t len)
{
	for (size_t i=0; i<len; ++i) {
		csum[0] = csum[0] + data[i];
		csum[1] = csum[1] + csum[0];
	}
}

/**
 * Send data using the UBX protocol
 */
int _ModuleGPS::ubxWrite(uint8_t msgClass, uint8_t msgId, const uint8_t * data, const size_t len) {
	uint8_t hdr[6];
	uint8_t csum[2];
	int ret;
	csum[0] = csum[1] = 0;

	TRACE_LOGD(TAG, "Sending ubx message {class=%02x, id=%02x, ln=%d}", msgClass, msgId, len);

	hdr[0] = 0xB5;
	hdr[1] = 0x62;
	hdr[2] = msgClass;
	hdr[3] = msgId;
	hdr[4] = len & 0xFF;
	hdr[5] = (len >> 8) & 0xFF;
	ubx_checksum_update(csum, &hdr[2], 4);

	// Send header
  ESP_LOG_BUFFER_HEXDUMP(TAG, hdr, sizeof(hdr), ESP_LOG_DEBUG);
  ret = ModuleUART0.write((char*)hdr, sizeof(hdr));
  if (ret < 0) {
  	return ret;
  }

  // Send data
  if (len > 0) {
	  ESP_LOG_BUFFER_HEXDUMP(TAG, data, len, ESP_LOG_DEBUG);
	  ret = ModuleUART0.write((char*)data, len);
	  if (ret < 0) {
	  	return ret;
	  }
  }

  // Send checksum
	ubx_checksum_update(csum, data, len);
  ESP_LOG_BUFFER_HEXDUMP(TAG, csum, 2, ESP_LOG_DEBUG);
  ret = ModuleUART0.write((char*)csum, 2);
  if (ret < 0) {
  	return ret;
  }

  return sizeof(hdr) + sizeof(csum) + len;
}

void _ModuleGPS::ubxSendTime()
{
	uint8_t time_info[24];
	uint16_t v16;
  time_t time_raw_format;
  struct tm * ti;
  int ret;

	TRACE_LOGD(TAG, "Sending UNIX timestamp");

  time ( &time_raw_format );
  ti = localtime ( &time_raw_format );

  memset(time_info, 0, sizeof(time_info));

	time_info[0] = 0x10; // Message type
	time_info[1] = 0x00; // Version
	time_info[2] = 0x00; // Immediately apply time
	time_info[3] = 0x80; // No leap second information

	v16 = ti->tm_year + 1900;
	time_info[4] =  v16 & 0xFF;
	time_info[5] = (v16 >> 8) & 0xFF; // year

	time_info[6] =  ti->tm_mon + 1; 	// Month
	time_info[7] =  ti->tm_mday; 			// Day
	time_info[8] =  ti->tm_hour; 			// Hour
	time_info[9] =  ti->tm_min; 			// Minute
	time_info[10] =  ti->tm_sec; 			// Seconds

	v16 = 60; // Give it 1h tollerancee
	time_info[16] =  v16 & 0xFF; 			// Seconds of accuracy
	time_info[17] = (v16 >> 8)& 0xFF;

	ret = ubxWrite(0x13, 0x40, time_info, sizeof(time_info));
	if (ret < 0) {
		TRACE_LOGE(TAG, "Could not update date: error=%d", ret);
	} else {
		TRACE_LOGI(TAG, "Pushed current date to GPS module");
	}
}

